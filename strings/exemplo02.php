<?php
// ToUpperCase():

    $nome = 'Jhonata dos santos';
    echo strtoupper($nome);

    // ToLowerCase():
    echo '<br/>' . strtolower($nome);

    // Cade letra de cada palavra para UpperCase:
    echo '<br/>' . ucwords($nome);

    // Primeira letra para UpperCase:
    echo '<br/>' . ucFirst($nome);
?>